    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        //JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("ARCHIVOS EXCEL", "xlsx");
        filechoser.setFileFilter(filter);
        int op = filechoser.showOpenDialog(this);
        if (op == JFileChooser.APPROVE_OPTION) {
            //////////////
            String path = filechoser.getSelectedFile().getPath();
            txtPath.setText(path);
            this.btnIniciar.setEnabled(false);
            getGlassPane().activate("Leyendo archivo...");
            this.leerExcel = new THleerExcel(glassPane, path, this.tablaDatos, this.btnIniciar, this.btnPaulaPab, this.txtaSalida);
            this.leerExcel.execute();

        }
    }//GEN-LAST:event_btnIniciarActionPerformed

