    private void procesarAulas() {
        List<Data> datass = tablaDatos.getMmodelo().getData();
        LinkedHashSet<String> pabellones = new LinkedHashSet<>();

        datass.forEach((data) -> {
            pabellones.add(data.getPabellon().trim());
            //System.out.println("::" + data.getNumero());
        });
        //System.out.println("pab: " + pabellones);
        getGlassPane().activate("Procesando pabellones y aulas...");
        this.procesaAulasPabellones = new THprocesaAulasPabellones(datass, pabellones, glassPane, btnIniciar, this.txtaSalida);
        this.procesaAulasPabellones.execute();
        //        datass.forEach((data) -> {
        //            System.out.println("aula:" + data.getAula() + " insertamos en buscar(" + data.getPabellon() + ")");
        //        });
    }

    private void procesarEstudiantes() {
        List<Data> datass = tablaDatos.getMmodelo().getData();
        Examen ex = (Examen) jComboBox1.getSelectedItem();
        long examen_id = ex.getId();
        //System.out.println("pab: " + pabellones);
        getGlassPane().activate("Procesando estudiantes...");
        this.tHprocesaEstudiantes = new THprocesaEstudiantes(datass, glassPane, examen_id, this.txtaSalida);
        this.tHprocesaEstudiantes.execute();
    }

